          <ul role="list">
            <li>Not having an official plan</li>
            <li>Not updating a plan over time (at major lifetime events)</li>
            <li>Not making arrangements for if they become incapacitated (disability or long-term care)</li>
            <li>Improper ownership of assets (how easy will it be to pass assets on)</li>
            <li>Not including charitable gifts</li>
            <li>Not appointing a guardian for children or others who would need their care</li>
            <li>Underestimating the implication of taxes</li>
            <li>Not having liquidity of assets</li>
            <li>Not making gifts during their lifetime to reduce the value of the estate after passing (tax advantages)</li>
            <li>Putting their child’s name on the deed to property (potentially huge tax implications)</li>
          </ul>
