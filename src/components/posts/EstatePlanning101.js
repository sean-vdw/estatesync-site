          <ul role="list">
            <li><b>Estate Tax:</b> A tax imposed on estates worth more than a set value. The tax is only assessed on the amount that exceeds the maximum, not the entire value of the estate.</li>
            <li><b>Inheritance Tax:</b> A tax paid by someone who inherits either property or money from someone who has died.</li>
            <li><b>Gift Tax:</b> A tax that’s applied on gifts exceeding a certain dollar amount. Note the giver, not the receiver, is responsible for any tax.</li>
          </ul>
